package br.com.DTO;

public class LabDTO {
    private int id_lab, quant_equip;
    private String nome_lab;

    public int getId_lab() {
        return id_lab;
    }

    public void setId_lab(int id_lab) {
        this.id_lab = id_lab;
    }

    public int getQuant_equip() {
        return quant_equip;
    }

    public void setQuant_equip(int quant_equip) {
        this.quant_equip = quant_equip;
    }

    public String getNome_lab() {
        return nome_lab;
    }

    public void setNome_lab(String nome_lab) {
        this.nome_lab = nome_lab;
    }
    
    
}
