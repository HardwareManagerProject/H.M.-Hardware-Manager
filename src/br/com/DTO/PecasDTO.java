package br.com.DTO;

public class PecasDTO {
    private int id_peca, id_equip, quant_peca;
    private String nome_peca, status_peca, desc_peca;

    public int getId_peca() {
        return id_peca;
    }

    public void setId_peca(int id_peca) {
        this.id_peca = id_peca;
    }

    public int getId_equip() {
        return id_equip;
    }

    public void setId_equip(int id_equip) {
        this.id_equip = id_equip;
    }

    public int getQuant_peca() {
        return quant_peca;
    }

    public void setQuant_peca(int quant_peca) {
        this.quant_peca = quant_peca;
    }

    public String getNome_peca() {
        return nome_peca;
    }

    public void setNome_peca(String nome_peca) {
        this.nome_peca = nome_peca;
    }

    public String getStatus_peca() {
        return status_peca;
    }

    public void setStatus_peca(String status_peca) {
        this.status_peca = status_peca;
    }

    public String getDesc_peca() {
        return desc_peca;
    }

    public void setDesc_peca(String desc_peca) {
        this.desc_peca = desc_peca;
    }
    
    
}
